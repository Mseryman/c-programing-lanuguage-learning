#include <stdio.h>

int main()
{
    int a;           // variable declaration as well as a is an integer
    a = 6;           // a is assigned the value 6
    float b = 5.5;   // b is a float with value 5.5
    char c = 'a';    // c is a character with value 'a' and always we have to write the values in single quotes
    printf("%d\n", a); // means prin a as a integer.
    
    /* we use %d for integers, %f for floats and %c for chars. for eg.-
    */
   printf("%f\n", b);
   printf("%c\n", c);
}